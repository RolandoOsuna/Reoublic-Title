      var threeyears = new Date();
      var fouryears = new Date();
      var fiveyears = new Date();
      var sixyears = new Date();
      var sevenyears = new Date();

      twoyears.setYear(twoyears.getFullYear() - 2);
      threeyears.setYear(threeyears.getFullYear() - 3);
      fouryears.setYear(fouryears.getFullYear() - 4);
      fiveyears.setYear(fiveyears.getFullYear() - 5);
      sixyears.setYear(sixyears.getFullYear() - 6);
